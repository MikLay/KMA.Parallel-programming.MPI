package com.mathpar.LABS.lab04;

import mpi.MPI;

import java.util.Arrays;

public class Task3 {
    public static void main(String[] args)
            throws Exception {
        MPI.Init(args);
        // rank
        int myrank = MPI.COMM_WORLD.getRank();

        // size
        int np = MPI.COMM_WORLD.getSize();
        int n = 5 * (myrank + 1);
        int[] a = new int[n];

        // recvcount
        int[] recvcount = new int[np];

        for (int i = 0; i < np; i++) {
            recvcount[i] = 5 * (i + 1);
        }

        // displs
        int[] displs = new int[np];
        for(int i = 0, m = 0; i < np; i++){
            displs[i] = m;
            m += recvcount[i];
        }

        for (int i = 0; i < n; i++) {
            a[i] = myrank;
        }
        System.out.println("rank = " + myrank + " : array = "
                + Arrays.toString(a));

        int arraySize = (5 + (5 * np)) * np / 2;
        int[] q = new int[arraySize];

        MPI.COMM_WORLD.gatherv(a, n, MPI.INT, q, recvcount, displs, MPI.INT,
                3);

        if (myrank == 3)
            System.out.println("rank = " + myrank + " : final array = "
                    + Arrays.toString(q));
        MPI.Finalize();
    }
}

